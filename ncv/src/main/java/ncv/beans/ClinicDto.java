package ncv.beans;

public class ClinicDto {
	
	private int clinicNo;
	private String clinicName;
	private String clinicTel;
	private String clinicTime;
	private String clinicPostcode;
	private String clinicAddress;
	private String clinicDetailAddress;
	
	public int getClinicNo() {
		return clinicNo;
	}
	public void setClinicNo(int clinicNo) {
		this.clinicNo = clinicNo;
	}
	public String getClinicName() {
		return clinicName;
	}
	public void setClinicName(String clinicName) {
		this.clinicName = clinicName;
	}
	public String getClinicTel() {
		return clinicTel;
	}
	public void setClinicTel(String clinicTel) {
		this.clinicTel = clinicTel;
	}
	public String getClinicTime() {
		return clinicTime;
	}
	public void setClinicTime(String clinicTime) {
		this.clinicTime = clinicTime;
	}
	public String getClinicPostcode() {
		return clinicPostcode;
	}
	public void setClinicPostcode(String clinicPostcode) {
		this.clinicPostcode = clinicPostcode;
	}
	public String getClinicAddress() {
		return clinicAddress;
	}
	public void setClinicAddress(String clinicAddress) {
		this.clinicAddress = clinicAddress;
	}
	public String getClinicDetailAddress() {
		return clinicDetailAddress;
	}
	public void setClinicDetailAddress(String clinicDetailAddress) {
		this.clinicDetailAddress = clinicDetailAddress;
	}
}
